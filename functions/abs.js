/**
 * Returns the absolute value of a number.
 *
 * @param {number} num - The number to get the absolute value of.
 * @return {number} The absolute value of the input number.
 * @example
 * const absoluteValue = abs(-5); // Returns 5
 */
function abs(num) {
    return Math.abs(num);
}

module.exports = {
    abs
};
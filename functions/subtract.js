/**
 * Subtracts one number from another.
 *
 * @param {number} num1 - The first number.
 * @param {number} num2 - The second number.
 * @return {number} The result of subtracting num2 from num1.
 *
 * @example
 * // returns 3
 * subtract(7, 4);
 */
function subtract(num1, num2) {
    return num1 - num2;
}

module.exports = {
    subtract
};
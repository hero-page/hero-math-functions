/**
 * Rounds a number up to the nearest integer.
 *
 * @param {number} num - The input number to be rounded up.
 * @return {number} The rounded up integer value of the input number.
 *
 * @example
 * const result = ceil(3.2); // returns 4
 */
function ceil(num) {
    return Math.ceil(num);
}

module.exports = {
    ceil
};